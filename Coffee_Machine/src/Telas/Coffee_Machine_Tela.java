    private void acao(int indice, JLabel campo){
        if(preco[indice]<=total){
            total-= preco[indice];
            lbvisor.setText(null);
            BigDecimal valor = new BigDecimal(total);
            NumberFormat nf=NumberFormat.getCurrencyInstance();
            lbtroco.setText(nf.format(valor));
            campo.setText(null);
            
            //ImageIcon imagem = new ImageIcon(img[indice]);
            //Image imag = imagem.getImage().getScaledInstance(img10.getWidth(), img10.getHeight(), Image.SCALE_DEFAULT);
            //img10.setIcon(new ImageIcon(imag));
            
            String sql = "update tb01_produtos set tb01_qtde = "+quantidade[indice]+" where tb01_cod_produto = "+codigo[indice];
            conn.runSQL(sql);
        }
    }


