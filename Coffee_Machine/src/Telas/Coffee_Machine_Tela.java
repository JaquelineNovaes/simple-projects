    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
       this.getContentPane().setBackground(new java.awt.Color(176,133,92));
    
       int i=0;
       try{
           conn.conectar();
           ResultSet rs = conn.buscacafe();
           while(rs.next()){
               img[i] = Toolkit.getDefaultToolkit().createImage((rs.getBytes("tb01_foto")));
               preco[i]= rs.getDouble("tb01_preco");
               quantidade [i]=rs.getInt("tb01_qtde");
               codigo[i]=rs.getInt("tb01_cod_produto");
               i++;
           }
           conn.desconectar();
       } catch(Exception ex)
        {
            System.out.println("Erro: "+ex);
        }
       
       JLabel[]rotulo = new JLabel[]{preco1,preco2, preco3, preco4,preco5, preco6, preco7, preco8, preco9};
       JLabel[]foto = new JLabel[]{img1,img2,img3,img4,img5,img6,img7,img8,img9};
       JButton botao[] = new JButton[] {btn1, btn2, btn3, btn4, btn5, btn6, btn7, btn8, btn9};
       for(int j=0;j<9;j++){
           BigDecimal valor = new BigDecimal(preco[j]);
           NumberFormat nf = NumberFormat.getCurrencyInstance();
           rotulo[j].setText(nf.format(valor));
           
           ImageIcon imagem = new ImageIcon(img[j]);
           Image imag = imagem.getImage().getScaledInstance(foto[j].getWidth(), foto[j].getHeight(), Image.SCALE_DEFAULT);
           foto[j].setIcon(new ImageIcon(imag));
           
           if(quantidade[j]==0){
               botao[j].setEnabled(false);
           }
       }
    }//GEN-LAST:event_formWindowOpened

