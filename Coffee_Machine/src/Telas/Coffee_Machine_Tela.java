    private void movimento (JLabel img, JLabel destino){
        new Thread(){
            public int x = img.getX(), y = img.getY(); 
        public void run(){
            while(x < destino.getX()){
                x++;
                img.setLocation(x,y);
              
                try{
                    sleep(6);
                }catch(Exception ex){
                    System.out.println("Erro: "+ex);
                }
            }
            
            while(y < destino.getY()){
                y++;
                img.setLocation(x,y);
                
                try{
                sleep(6);
                } catch (Exception ex){
                System.out.println("Erro: "+ex);
                }
            }  
        }
    }.start();
    }
    
